package com.zja.controller.parts3;

/*

大文件分片上传实现逻辑：
    前端实现（Vue.js）：
        前端需要负责文件的切片、上传、进度显示等功能。
    后端实现（Spring Boot）：
        接口1：初始化上传
            用于初始化上传，返回一个唯一的文件标识符（fileId），前端上传文件时需要携带该标识符。
        接口2：上传分片
            根据文件标识符、分片索引和分片数据，将分片数据保存到临时文件中。
        接口3：合并文件
            当所有分片上传完成后，根据文件标识符将所有分片合并成完整的文件。
        接口4：查询上传进度
            可选接口，用于查询上传进度，前端可以定时查询该接口获取上传进度信息。
 */